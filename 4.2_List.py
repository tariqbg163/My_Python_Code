# Searching Value in List Using index(value,startIndex,endIndex)
# It return the index of search value if it exist otherwise cause error

l=[10,11,12,13,10,15,16,10]
print(l.index(10)) # find the index of 10 ..note it start 10 value from  0...endIndex

print(l.index(10,2)) #Search 10 from index 2.....to...endIndex
print(l.index(10,3,6))# Search 10 from index 3...to...6 Index position
